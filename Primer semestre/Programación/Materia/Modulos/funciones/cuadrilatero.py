import math
# Definir 3 funciones... Cálculo perímeto, área y volúmen... CUADRILATERO O CUBO

def perimetro(ancho,largo):
    return ancho + largo * 2

def area(ancho,largo):
    return ancho * largo

def volumen(ancho,largo,alto):
    return ancho * largo * alto

print(perimetro(2,3))
print(area(2,3))
print(volumen(2,3,4))
