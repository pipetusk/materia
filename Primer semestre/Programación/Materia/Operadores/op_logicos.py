# Operador Y o AND, es verdadero cuando ambos valores son verdaderos

print("Operador Y")
print(True & True)
print(True & False)
print(False & True)
print(False & False)

# Operador O u OR, es verdadero cuando al menos un valor es verdadero

print("Operador O")
print(True | True)
print(True | False)
print(False | True)
print(False | False)

# Operador NEGACIÓN NOT
print ("Negación, NOT")
print(not True)
print(not False) 