# Definición de variables
numero_1 = 5
numero_2 = 7
numero_3 = 15
numero_4 = 674238539536438146

nombre = "Tiraba"
apellido = "Cualquiera"

# Operador SUMA

suma = numero_1 + numero_2
print(suma)

# Concatenación

nombre_completo = nombre + " " + apellido 
print(nombre_completo)

# Operador RESTA 

resta = numero_1 - numero_2
print(resta)

# Operador MULTIPLICACIÓN

multiplicacion = numero_1 * numero_2
print(multiplicacion)
print(nombre * 3)

# Operador EXPONENTE

expontente = numero_1 ** numero_2 # 5^7
print(expontente)

# Operador DIVISIÓN

division = numero_3 / numero_1
print(division)
print(type(division))

# Operador DIVISIÓN BAJA

division_baja = numero_3 // numero_2
print(division_baja)
print(type(division_baja))
